from __future__ import annotations

import argparse
from pathlib import Path

import pandas as pd

from src.wisdm import (
    DEFAULT_DATASET_DIR,
    RAW_COLUMNS,
    class_distribution,
    clean_raw_data,
    load_raw_dataset,
    markdown_table,
    slug_for_selection,
)


def preprocess(
    dataset_dir: Path,
    device: str,
    sensor: str,
    interim_dir: Path,
    processed_dir: Path,
    report_path: Path,
) -> Path:
    interim_dir.mkdir(parents=True, exist_ok=True)
    processed_dir.mkdir(parents=True, exist_ok=True)
    slug = slug_for_selection(device, sensor)

    raw = load_raw_dataset(dataset_dir, device=device, sensor=sensor)
    standardized_path = interim_dir / f"{slug}_standardized.csv.gz"
    raw.to_csv(standardized_path, index=False, compression="gzip")

    missing_before = (
        raw[RAW_COLUMNS + ["activity_name"]]
        .isna()
        .sum()
        .rename("missing_rows")
        .reset_index()
        .rename(columns={"index": "column"})
    )
    missing_before["missing_percent"] = (missing_before["missing_rows"] / len(raw) * 100).round(4)

    clean, dropped_rows, counts = clean_raw_data(raw)
    dropped_path = interim_dir / f"{slug}_dropped_rows.csv.gz"
    dropped_rows.to_csv(dropped_path, index=False, compression="gzip")

    cleaned_path = processed_dir / f"{slug}_clean.csv.gz"
    clean.to_csv(cleaned_path, index=False, compression="gzip")

    summary = pd.DataFrame([counts])
    summary["device"] = device
    summary["sensor"] = sensor
    summary.to_csv(interim_dir / f"{slug}_preprocessing_summary.csv", index=False)
    missing_before.to_csv(interim_dir / f"{slug}_missing_before_cleaning.csv", index=False)

    label_dist = class_distribution(clean)
    label_dist.to_csv(processed_dir / f"{slug}_class_distribution_clean.csv", index=False)

    report_lines = [
        "# Preprocessing Report",
        "",
        f"Selected stream: `{device}` `{sensor}`.",
        "",
        "## Pipeline steps",
        "",
        "1. Loaded every selected raw subject file.",
        "2. Standardized column names to `subject_id`, `activity_label`, `timestamp_ns`, `x`, `y`, `z` and added `device`, `sensor`, `source_file`, and `activity_name`.",
        "3. Converted subject IDs and timestamps to integer nullable types, and sensor axes to numeric floats.",
        "4. Normalized activity labels to uppercase and mapped labels to activity names.",
        "5. Dropped rows with missing required fields, unknown labels, nonpositive timestamps, or subject/file mismatches.",
        "6. Removed exact duplicate samples.",
        "7. Sorted the cleaned data by subject, activity, and timestamp.",
        "",
        "## Row counts",
        "",
        markdown_table(summary[["device", "sensor", "input_rows", "invalid_rows", "duplicate_rows_removed", "output_rows"]]),
        "",
        "## Missing values before cleaning",
        "",
        markdown_table(missing_before),
        "",
        "## Clean class distribution",
        "",
        markdown_table(label_dist),
        "",
        "## Saved outputs",
        "",
        f"- Standardized intermediate data: `{standardized_path}`",
        f"- Dropped-row intermediate data: `{dropped_path}`",
        f"- Clean processed data: `{cleaned_path}`",
        f"- Summary CSV: `{interim_dir / f'{slug}_preprocessing_summary.csv'}`",
        "",
        "## Notes",
        "",
        "The cleaned dataset keeps raw sensor samples rather than precomputed ARFF features so that downstream windowing and split assignment remain under project control.",
        "",
        "Generated by `preprocess.py`.",
        "",
    ]
    report_path.write_text("\n".join(report_lines), encoding="utf-8")
    return cleaned_path


def main() -> None:
    parser = argparse.ArgumentParser(description="Clean and standardize a selected WISDM raw stream.")
    parser.add_argument("--dataset-dir", type=Path, default=DEFAULT_DATASET_DIR)
    parser.add_argument("--device", default="phone", choices=["phone", "watch"])
    parser.add_argument("--sensor", default="accel", choices=["accel", "gyro"])
    parser.add_argument("--interim-dir", type=Path, default=Path("data/interim"))
    parser.add_argument("--processed-dir", type=Path, default=Path("data/processed"))
    parser.add_argument("--report", type=Path, default=Path("preprocessing_report.md"))
    args = parser.parse_args()

    cleaned_path = preprocess(args.dataset_dir, args.device, args.sensor, args.interim_dir, args.processed_dir, args.report)
    print(f"Wrote {cleaned_path}")
    print(f"Wrote {args.report}")


if __name__ == "__main__":
    main()
