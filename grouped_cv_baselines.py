from __future__ import annotations

import argparse
from pathlib import Path

import pandas as pd
from sklearn.metrics import accuracy_score, f1_score
from sklearn.model_selection import GroupKFold
from sklearn.preprocessing import LabelEncoder

from baselines import make_models
from src.wisdm import feature_columns, markdown_table


def run_grouped_cv(
    windows_path: Path,
    out_dir: Path,
    report_path: Path,
    label_order: list[str],
    seed: int,
    n_splits: int,
    include_test: bool,
) -> None:
    out_dir.mkdir(parents=True, exist_ok=True)
    windows = pd.read_csv(windows_path)
    if not include_test and "split" in windows.columns:
        windows = windows[windows["split"].isin(["train", "val"])].copy()

    features = feature_columns(windows)
    label_encoder = LabelEncoder().fit(label_order)
    groups = windows["subject_id"].to_numpy()
    x = windows[features]
    y = label_encoder.transform(windows["activity_label"])
    labels_present = list(range(len(label_encoder.classes_)))

    unique_groups = pd.Series(groups).nunique()
    folds = min(n_splits, unique_groups)
    cv = GroupKFold(n_splits=folds)
    rows = []
    for fold_idx, (train_idx, eval_idx) in enumerate(cv.split(x, y, groups=groups), start=1):
        train_subjects = pd.Series(groups[train_idx]).nunique()
        eval_subjects = pd.Series(groups[eval_idx]).nunique()
        for model_name, model in make_models(seed + fold_idx).items():
            model.fit(x.iloc[train_idx], y[train_idx])
            pred = model.predict(x.iloc[eval_idx])
            rows.append(
                {
                    "fold": fold_idx,
                    "model": model_name,
                    "train_subjects": train_subjects,
                    "eval_subjects": eval_subjects,
                    "eval_windows": len(eval_idx),
                    "accuracy": round(accuracy_score(y[eval_idx], pred), 4),
                    "macro_f1": round(f1_score(y[eval_idx], pred, average="macro", labels=labels_present), 4),
                }
            )

    fold_metrics = pd.DataFrame(rows)
    fold_metrics.to_csv(out_dir / "groupkfold_fold_metrics.csv", index=False)
    summary = (
        fold_metrics.groupby("model", observed=True)
        .agg(
            folds=("fold", "nunique"),
            accuracy_mean=("accuracy", "mean"),
            accuracy_std=("accuracy", "std"),
            macro_f1_mean=("macro_f1", "mean"),
            macro_f1_std=("macro_f1", "std"),
        )
        .round(4)
        .reset_index()
        .sort_values("macro_f1_mean", ascending=False)
    )
    summary.to_csv(out_dir / "groupkfold_summary.csv", index=False)

    scope = "all windows" if include_test else "train+validation windows only"
    lines = [
        "# Grouped CV Baseline Stability",
        "",
        f"Window feature table: `{windows_path}`",
        f"Scope: {scope}.",
        "",
        "GroupKFold uses `subject_id` as the group, so no subject appears in both training and evaluation within a fold. This is a stability analysis for the classical baselines; the final held-out test split remains the primary result.",
        "",
        "## Summary",
        "",
        markdown_table(summary),
        "",
        "## Per-fold metrics",
        "",
        markdown_table(fold_metrics),
        "",
        "Generated by `grouped_cv_baselines.py`.",
        "",
    ]
    report_path.write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser(description="Run GroupKFold stability checks for classical WISDM baselines.")
    parser.add_argument("--windows", type=Path, required=True)
    parser.add_argument("--out-dir", type=Path, default=Path("data/processed/clean3/grouped_cv"))
    parser.add_argument("--report", type=Path, default=Path("grouped_cv_clean3_report.md"))
    parser.add_argument("--label-order", default="A,B,E")
    parser.add_argument("--seed", type=int, default=20260418)
    parser.add_argument("--n-splits", type=int, default=5)
    parser.add_argument("--include-test", action="store_true", help="Include held-out test subjects in CV stability analysis.")
    args = parser.parse_args()
    label_order = [label.strip().upper() for label in args.label_order.split(",")]
    run_grouped_cv(args.windows, args.out_dir, args.report, label_order, args.seed, args.n_splits, args.include_test)
    print(f"Wrote {args.report}")


if __name__ == "__main__":
    main()
