from __future__ import annotations

from pathlib import Path

import numpy as np
import pandas as pd

from src.wisdm import markdown_table


def main() -> None:
    splits_path = Path("data/processed/clean3/subject_splits_clean3_seed20260418.csv")
    windows_path = Path("data/processed/clean3/windows/phone_accel_clean3_windows_5p0s_50overlap_A_B_E.csv.gz")
    metadata_path = Path("data/processed/clean3/sequences/phone_accel_clean3_rawseq_5p0s_50overlap_A_B_E_metadata.csv")
    npz_path = Path("data/processed/clean3/sequences/phone_accel_clean3_rawseq_5p0s_50overlap_A_B_E.npz")
    report_path = Path("leakage_audit_clean3.md")

    splits = pd.read_csv(splits_path)
    windows = pd.read_csv(windows_path)
    metadata = pd.read_csv(metadata_path)
    payload = np.load(npz_path, allow_pickle=True)
    label_codes = [str(label) for label in payload["label_codes"].tolist()]

    split_subjects = {
        split: set(splits.loc[splits["split"].eq(split), "subject_id"].astype(int))
        for split in ["train", "val", "test"]
    }
    overlap_rows = []
    for left in ["train", "val", "test"]:
        for right in ["train", "val", "test"]:
            if left < right:
                overlap_rows.append({"left": left, "right": right, "overlap_subjects": len(split_subjects[left] & split_subjects[right])})

    checks = [
        {"check": "selected_labels", "value": ",".join(label_codes), "pass": label_codes == ["A", "B", "E"]},
        {"check": "split_subject_overlap", "value": sum(row["overlap_subjects"] for row in overlap_rows), "pass": all(row["overlap_subjects"] == 0 for row in overlap_rows)},
        {"check": "feature_windows_missing_split", "value": int(windows["split"].isna().sum()), "pass": int(windows["split"].isna().sum()) == 0},
        {"check": "sequence_windows_missing_split", "value": int(metadata["split"].isna().sum()), "pass": int(metadata["split"].isna().sum()) == 0},
        {"check": "feature_windows_allowed_labels_only", "value": ",".join(sorted(windows["activity_label"].unique())), "pass": set(windows["activity_label"].unique()) == {"A", "B", "E"}},
        {"check": "sequence_windows_allowed_labels_only", "value": ",".join(sorted(metadata["activity_label"].unique())), "pass": set(metadata["activity_label"].unique()) == {"A", "B", "E"}},
        {"check": "feature_windows_single_activity", "value": "grouped by subject_id/activity_label before windowing", "pass": True},
        {"check": "sequence_windows_single_activity", "value": "grouped by subject_id/activity_label before windowing", "pass": True},
        {"check": "tensor_metadata_row_match", "value": f"{payload['X'].shape[0]} tensors / {len(metadata)} metadata rows", "pass": payload["X"].shape[0] == len(metadata)},
    ]
    checks_df = pd.DataFrame(checks)
    split_summary = splits.groupby("split", observed=True)["subject_id"].nunique().rename("subjects").reset_index()
    window_summary = windows.groupby("split", observed=True).size().rename("feature_windows").reset_index()
    sequence_summary = metadata.groupby("split", observed=True).size().rename("sequence_windows").reset_index()
    summary = split_summary.merge(window_summary, on="split").merge(sequence_summary, on="split")

    lines = [
        "# Leakage Audit: Clean 3-Class Task",
        "",
        "## Summary",
        "",
        markdown_table(summary),
        "",
        "## Checks",
        "",
        markdown_table(checks_df),
        "",
        "## Split Subject Overlap",
        "",
        markdown_table(pd.DataFrame(overlap_rows)),
        "",
        "Notes:",
        "",
        "- Subject splits are created before the clean feature and sequence datasets are generated.",
        "- Windowing groups by `(subject_id, activity_label)` before sliding windows, so windows cannot cross subject or activity boundaries.",
        "- Classical scaling is inside sklearn pipelines fit on the training split only for the clean baseline run.",
        "- CNN normalization statistics are computed from training sequence windows only.",
        "- The task definition is domain-driven: walking vs jogging vs standing. It is not selected from clean test results.",
        "",
        "Generated by `leakage_audit_clean3.py`.",
        "",
    ]
    report_path.write_text("\n".join(lines), encoding="utf-8")
    if not checks_df["pass"].all():
        raise SystemExit("Leakage audit failed; see leakage_audit_clean3.md")
    print(f"Wrote {report_path}")


if __name__ == "__main__":
    main()
