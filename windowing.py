from __future__ import annotations

import argparse
from pathlib import Path

import pandas as pd

from src.wisdm import class_distribution, make_windows, markdown_table, slug_for_selection


DEFAULT_CANDIDATES = [(2.5, 0.50), (5.0, 0.50), (10.0, 0.50)]


def candidate_name(device: str, sensor: str, window_seconds: float, overlap: float, labels: list[str] | None = None, dataset_name: str | None = None) -> str:
    win = str(window_seconds).replace(".", "p")
    ov = str(int(round(overlap * 100)))
    prefix = dataset_name if dataset_name else slug_for_selection(device, sensor)
    suffix = "" if labels is None else "_" + "_".join(labels)
    return f"{prefix}_windows_{win}s_{ov}overlap{suffix}.csv.gz"


def add_split_column(windows: pd.DataFrame, splits_path: Path) -> pd.DataFrame:
    if not splits_path.exists():
        return windows
    splits = pd.read_csv(splits_path)
    return windows.merge(splits, on="subject_id", how="left")


def build_windows_for_candidates(
    clean_data: Path,
    splits_path: Path,
    out_dir: Path,
    summary_path: Path,
    device: str,
    sensor: str,
    sample_rate_hz: float,
    candidates: list[tuple[float, float]],
    labels: list[str] | None = None,
    dataset_name: str | None = None,
) -> None:
    out_dir.mkdir(parents=True, exist_ok=True)
    df = pd.read_csv(clean_data)
    if labels is not None:
        df = df[df["activity_label"].isin(labels)].copy()
        if df.empty:
            raise ValueError("No rows remain after label filtering.")
    splits = pd.read_csv(splits_path)
    df = df.merge(splits, on="subject_id", how="left")
    if df["split"].isna().any():
        raise ValueError("Some raw rows have no split assignment.")
    rows = []
    dist_tables = []

    for window_seconds, overlap in candidates:
        split_windows = []
        for split_name, split_df in df.groupby("split", observed=True, sort=True):
            windows_part = make_windows(
                split_df.drop(columns=["split"]),
                window_seconds=window_seconds,
                overlap=overlap,
                sample_rate_hz=sample_rate_hz,
            )
            windows_part["split"] = split_name
            split_windows.append(windows_part)
        windows = pd.concat(split_windows, ignore_index=True)
        out_path = out_dir / candidate_name(device, sensor, window_seconds, overlap, labels=labels, dataset_name=dataset_name)
        windows.to_csv(out_path, index=False, compression="gzip")

        dist = class_distribution(windows)
        dist["window_seconds"] = window_seconds
        dist["overlap"] = overlap
        dist_tables.append(dist)

        rows.append(
            {
                "window_seconds": window_seconds,
                "overlap": overlap,
                "samples_per_window": int(round(window_seconds * sample_rate_hz)),
                "step_samples": int(round(window_seconds * sample_rate_hz * (1 - overlap))),
                "windows": len(windows),
                "subjects": windows["subject_id"].nunique(),
                "classes": windows["activity_label"].nunique(),
                "output": str(out_path),
            }
        )

    summary = pd.DataFrame(rows)
    summary.to_csv(out_dir / "windowing_candidates.csv", index=False)
    if dist_tables:
        pd.concat(dist_tables, ignore_index=True).to_csv(out_dir / "windowing_class_distributions.csv", index=False)

    lines = [
        "# Windowing Summary",
        "",
        "Windows are generated within each `(subject_id, activity_label)` group after sorting by timestamp. This prevents windows from crossing subject or activity boundaries, so labels are inherited cleanly from the source samples.",
        "",
        "## Candidate settings",
        "",
        markdown_table(summary),
        "",
        "## Recommendation",
        "",
        "Use 5 second windows with 50% overlap as the main baseline setting. It balances temporal context against sample count and matches common human-activity-recognition practice better than very short windows while producing more training windows than 10 second windows.",
        "",
        "Generated by `windowing.py`.",
        "",
    ]
    summary_path.write_text("\n".join(lines), encoding="utf-8")


def parse_candidates(raw_values: list[str] | None) -> list[tuple[float, float]]:
    if not raw_values:
        return DEFAULT_CANDIDATES
    parsed = []
    for value in raw_values:
        window, overlap = value.split(",")
        parsed.append((float(window), float(overlap)))
    return parsed


def main() -> None:
    parser = argparse.ArgumentParser(description="Create fixed WISDM windows and feature tables.")
    parser.add_argument("--clean-data", type=Path, default=Path("data/processed/phone_accel_clean.csv.gz"))
    parser.add_argument("--splits", type=Path, default=Path("data/processed/subject_splits.csv"))
    parser.add_argument("--out-dir", type=Path, default=Path("data/processed/windows"))
    parser.add_argument("--summary", type=Path, default=Path("windowing_summary.md"))
    parser.add_argument("--device", default="phone")
    parser.add_argument("--sensor", default="accel")
    parser.add_argument("--dataset-name", default=None, help="Optional prefix for saved window artifacts.")
    parser.add_argument("--sample-rate-hz", type=float, default=20.0)
    parser.add_argument("--candidate", action="append", help="Candidate as window_seconds,overlap. May be repeated.")
    parser.add_argument("--include-labels", default=None, help="Comma-separated activity labels to keep, for example A,B,E.")
    args = parser.parse_args()
    labels = [label.strip().upper() for label in args.include_labels.split(",")] if args.include_labels else None

    build_windows_for_candidates(
        args.clean_data,
        args.splits,
        args.out_dir,
        args.summary,
        args.device,
        args.sensor,
        args.sample_rate_hz,
        parse_candidates(args.candidate),
        labels=labels,
        dataset_name=args.dataset_name,
    )
    print(f"Wrote {args.summary}")


if __name__ == "__main__":
    main()
