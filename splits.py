from __future__ import annotations

import argparse
from pathlib import Path

import numpy as np
import pandas as pd

from src.wisdm import ACTIVITY_ORDER, markdown_table


def choose_subject_split(
    df: pd.DataFrame,
    seed: int,
    labels: list[str] | None = None,
    train_fraction: float = 0.70,
    val_fraction: float = 0.15,
    max_attempts: int = 1000,
) -> pd.DataFrame:
    if labels is not None:
        df = df[df["activity_label"].isin(labels)].copy()
        if df.empty:
            raise ValueError("No rows remain after label filtering.")
    subjects = np.array(sorted(df["subject_id"].dropna().unique()))
    n_subjects = len(subjects)
    n_train = int(round(n_subjects * train_fraction))
    n_val = int(round(n_subjects * val_fraction))
    n_train = min(max(n_train, 1), n_subjects - 2)
    n_val = min(max(n_val, 1), n_subjects - n_train - 1)

    subject_labels = df.groupby("subject_id", observed=True)["activity_label"].agg(lambda s: set(s)).to_dict()
    all_labels = set(labels if labels is not None else sorted(df["activity_label"].dropna().unique()))

    best = None
    best_score = -1
    for offset in range(max_attempts):
        rng = np.random.default_rng(seed + offset)
        shuffled = subjects.copy()
        rng.shuffle(shuffled)
        train_subjects = set(shuffled[:n_train])
        val_subjects = set(shuffled[n_train : n_train + n_val])
        test_subjects = set(shuffled[n_train + n_val :])
        splits = {"train": train_subjects, "val": val_subjects, "test": test_subjects}

        coverage = {
            split: set().union(*(subject_labels[subj] for subj in split_subjects))
            for split, split_subjects in splits.items()
        }
        score = sum(len(labels & all_labels) for labels in coverage.values())
        if score > best_score:
            best = splits
            best_score = score
        if all(all_labels.issubset(labels) for labels in coverage.values()):
            best = splits
            break

    rows = []
    assert best is not None
    for split, split_subjects in best.items():
        for subject_id in sorted(split_subjects):
            rows.append({"subject_id": int(subject_id), "split": split})
    return pd.DataFrame(rows).sort_values(["split", "subject_id"]).reset_index(drop=True)


def write_protocol(df: pd.DataFrame, splits: pd.DataFrame, out_path: Path, labels: list[str] | None = None) -> None:
    if labels is not None:
        df = df[df["activity_label"].isin(labels)].copy()
    merged = df[["subject_id", "activity_label", "activity_name"]].merge(splits, on="subject_id", how="left")
    subject_counts = splits.groupby("split", observed=True)["subject_id"].nunique().rename("subjects").reset_index()
    row_counts = merged.groupby("split", observed=True).size().rename("rows").reset_index()
    class_coverage = (
        merged.groupby(["split", "activity_label", "activity_name"], observed=True)
        .size()
        .rename("rows")
        .reset_index()
    )
    split_summary = subject_counts.merge(row_counts, on="split")
    split_summary["row_percent"] = (split_summary["rows"] / split_summary["rows"].sum() * 100).round(2)

    lines = [
        "# Evaluation Protocol",
        "",
        "## Split strategy",
        "",
        "Use a subject-wise split. Every raw sample and every downstream window from a given `subject_id` belongs to exactly one split.",
        "",
        "This avoids identity leakage: adjacent time-series samples from the same person are highly correlated, and a naive random row split would put near-duplicate movement patterns from the same subject into both train and test sets.",
        "",
        "Default split targets are approximately 70% train, 15% validation, and 15% test by subject. The script searches deterministic seeds until each split has coverage of all selected activity labels when possible.",
        "",
        "## Split summary",
        "",
        markdown_table(split_summary),
        "",
        "## Per-split class coverage",
        "",
        markdown_table(class_coverage),
        "",
        "## Recommended reporting",
        "",
        "- During model selection, fit preprocessing scalers and models on train windows only.",
        "- Use validation only for model choice and hyperparameter tuning.",
        "- After choosing a baseline configuration, optionally refit on train plus validation before the single held-out test evaluation.",
        "- Report final accuracy, macro F1, confusion matrix, and per-class metrics on the held-out test subjects.",
        "- For more robust estimates, use grouped cross-validation by `subject_id` as a secondary analysis.",
        "",
        "Generated by `splits.py`.",
        "",
    ]
    out_path.write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser(description="Create leakage-safe subject-wise WISDM splits.")
    parser.add_argument("--clean-data", type=Path, default=Path("data/processed/phone_accel_clean.csv.gz"))
    parser.add_argument("--out", type=Path, default=Path("data/processed/subject_splits.csv"))
    parser.add_argument("--protocol", type=Path, default=Path("evaluation_protocol.md"))
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--include-labels", default=None, help="Comma-separated activity labels to include before splitting, for example A,B,E.")
    args = parser.parse_args()

    df = pd.read_csv(args.clean_data)
    labels = [label.strip().upper() for label in args.include_labels.split(",")] if args.include_labels else None
    unknown_labels = sorted(set(labels or []) - set(ACTIVITY_ORDER))
    if unknown_labels:
        raise ValueError(f"Unknown activity labels: {unknown_labels}")
    splits = choose_subject_split(df, seed=args.seed, labels=labels)
    args.out.parent.mkdir(parents=True, exist_ok=True)
    splits.to_csv(args.out, index=False)

    split_index = df[["subject_id"]].drop_duplicates().merge(splits, on="subject_id", how="left")
    split_index.to_csv(args.out.parent / f"{args.out.stem}_index.csv", index=False)
    write_protocol(df, splits, args.protocol, labels=labels)
    print(f"Wrote {args.out}")
    print(f"Wrote {args.protocol}")


if __name__ == "__main__":
    main()
